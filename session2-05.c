#include <stdio.h>

int main() {
    
    int chieu_dai = 10;
    int chieu_rong = 5;

    
    int chu_vi = 2 * (chieu_dai + chieu_rong);
    int dien_tich = chieu_dai * chieu_rong;

    
    printf("Chu vi hinh chu nhat: %d\n", chu_vi);
    printf("Dien tich hinh chu nhat: %d\n", dien_tich);

    return 0;
}
